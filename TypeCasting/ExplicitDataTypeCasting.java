package TypeCasting;

public class ExplicitDataTypeCasting {
    public static void main(String args[]){
        double d=50.54;
        System.out.println("Double Representation "+d);
        float f=(float)d;
        System.out.println("Float Representation "+f);
        long l=(long)f;
        System.out.println("Long Representation "+l);
        int i=(int)l;
        System.out.println("Integer Regresentation "+i);
    }
}
